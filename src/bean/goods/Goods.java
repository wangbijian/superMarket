package bean.goods;

public class Goods {
	private String goodsId;
	private String goodsName;
	private int goodsInPrice;
	private int goodsOutPrice;
	private int goodsCount;
	public String getGoodsId() {
		return goodsId;
	}
	public void setGoodsId(String goodsId) {
		this.goodsId = goodsId;
	}
	public String getGoodsName() {
		return goodsName;
	}
	public void setGoodsName(String goodsName) {
		this.goodsName = goodsName;
	}
	public int getGoodsInPrice() {
		return goodsInPrice;
	}
	public void setGoodsInPrice(int goodsInPrice) {
		this.goodsInPrice = goodsInPrice;
	}
	public int getGoodsOutPrice() {
		return goodsOutPrice;
	}
	public void setGoodsOutPrice(int goodsOutPrice) {
		this.goodsOutPrice = goodsOutPrice;
	}
	public int getGoodsCount() {
		return goodsCount;
	}
	public void setGoodsCount(int goodsCount) {
		this.goodsCount = goodsCount;
	}
}
